    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno = jTextField20.getText();
         try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Manish@mj5");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"' and result.rollno='"+rollno+"'");
           if(rs.next()){
              jTextField17.setText(rs.getString(2));
              jTextField18.setText(rs.getString(4));
              jTextField19.setText(rs.getString(3));
              jTextField21.setText(rs.getString(5));
              
               jTextField11.setText(rs.getString(7));
               jTextField12.setText(rs.getString(8));
               jTextField13.setText(rs.getString(9));
               jTextField14.setText(rs.getString(10));
               jTextField15.setText(rs.getString(11));
               
               int s1 = Integer.parseInt(jTextField11.getText());
               int s2 = Integer.parseInt(jTextField12.getText());
               int s3 = Integer.parseInt(jTextField13.getText());
               int s4 = Integer.parseInt(jTextField14.getText());
               int s5 = Integer.parseInt(jTextField15.getText());
               if(s1<30 || s2<30 || s3<30|| s4<30 || s5<30){
                   jTextField23.setForeground(Color.red);
                   jTextField23.setText("Fail");
                   
               }
               else{
                   jTextField23.setForeground(Color.green);
                   jTextField23.setText("Pass");
               }
               int result = s1+s2+s3+s4+s5;
               jTextField16.setText(String.valueOf(result));
               
           }
          
        }
        catch(Exception e){
           JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

